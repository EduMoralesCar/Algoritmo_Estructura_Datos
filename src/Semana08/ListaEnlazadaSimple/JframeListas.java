    private Nodo Buscar(Nodo inicio, String marca) {
        Nodo actual = inicio;
        while (actual != null && !marca.equalsIgnoreCase(actual.marca)) {
            actual = actual.siguiente;
        }
        return actual;
    }

    private Nodo InsertarAlInicio(Nodo inicio, String marca, String nombre, String modelo, double precio, int stock) {
        Nodo nuevo = new Nodo(marca, nombre, modelo, precio, stock);
        nuevo.siguiente = inicio;
        inicio = nuevo;
        numero++;
        return inicio;
    }

    private void Eliminar(Nodo actual) {
        if (actual == null) {
            return;
        }

        if (actual == inicio) {
            inicio = inicio.siguiente;
        } else {
            Nodo anterior = inicio;
            while (anterior != null && anterior.siguiente != actual) {
                anterior = anterior.siguiente;
            }
            if (anterior != null) {
                anterior.siguiente = actual.siguiente;
            }
        }
        numero--;
    }

