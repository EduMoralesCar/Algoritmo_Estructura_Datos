    private void ButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConsultarActionPerformed
        String marca = jTextField1.getText();
        if (marca.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese la Marca por favor");
            return;
        }

        pFound = Buscar(inicio, marca);
        if (pFound != null) {
            jTextField1.setText(pFound.marca);
            jTextField2.setText(pFound.nombre);
            jTextField3.setText(pFound.modelo);
            jTextField4.setText(String.valueOf(pFound.precio));
            jTextField5.setText(String.valueOf(pFound.stock));
            Habilitar();
        } else {
            JOptionPane.showMessageDialog(this, "El nombre: " + marca + ", no estÃ¡ en la Lista");
        }
    }//GEN-LAST:event_ButtonConsultarActionPerformed

