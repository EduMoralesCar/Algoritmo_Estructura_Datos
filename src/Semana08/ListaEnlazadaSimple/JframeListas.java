    private void ButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActualizarActionPerformed
        if (pFound != null) {
            try {
                if (jTextField1.getText().isEmpty()
                        || jTextField2.getText().isEmpty()
                        || jTextField3.getText().isEmpty()
                        || jTextField4.getText().isEmpty()
                        || jTextField5.getText().isEmpty()) {

                    JOptionPane.showMessageDialog(this, "Todo los campos son obligatorios");
                    return;
                }
                pFound.marca = jTextField1.getText();
                pFound.nombre = jTextField2.getText();
                pFound.modelo = jTextField3.getText();
                pFound.precio = Double.parseDouble(jTextField4.getText());
                pFound.stock = Integer.parseInt(jTextField5.getText());

                LimpiarEntradas();
                Deshabilitar();
                VerDatos();

                JOptionPane.showMessageDialog(this, "Datos actualizados correctamente");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this,
                        "Error en formato:\n- Precio debe ser un nÃºmero (ej: 19.99)\n- Stock debe ser entero (ej: 10)",
                        "Error de formato",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_ButtonActualizarActionPerformed

    private void ButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGuardarActionPerformed
        String marca = jTextField1.getText();
        String nombre = jTextField2.getText();
        String modelo = jTextField3.getText();
        String precio = jTextField4.getText();
        String stock = jTextField5.getText();

        if (marca.isEmpty() || nombre.isEmpty() || modelo.isEmpty() || precio.isEmpty() || stock.isEmpty()) {
            //JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }

        try {
            double precioValor = Double.parseDouble(precio);
            int stockValor = Integer.parseInt(stock);

            inicio = InsertarAlInicio(inicio, marca, nombre, modelo, precioValor, stockValor);
            LimpiarEntradas();
            VerDatos();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Precio y Stock deben ser numeros");
        }


    }//GEN-LAST:event_ButtonGuardarActionPerformed

