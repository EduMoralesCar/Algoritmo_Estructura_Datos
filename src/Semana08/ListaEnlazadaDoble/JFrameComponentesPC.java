    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String marca = jComboBox1.getSelectedItem().toString();
        String nombre = jTextField1.getText();
        String modelo = jTextField2.getText().toUpperCase();
        String precio = jTextField3.getText();
        String stock = jTextField4.getText();

        if (nombre.isEmpty() || modelo.isEmpty() || precio.isEmpty() || stock.isEmpty()) {
            //JOptionPane.showMessageDialog(this, "Complete todo los campos");
            return;
        }

        try {
            double precioValor = Double.parseDouble(precio);
            int stockValor = Integer.parseInt(stock);

            // Llamada al metodo insertarAlFinal
            insertarAlFinal(nombre, marca, modelo, precioValor, stockValor);
            actualizarTabla();
            limpiarCampos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Precio y Stock deben ser número válidos");
            limpiarCampos();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

