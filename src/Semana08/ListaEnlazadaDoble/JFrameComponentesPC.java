    private Nodo insertarAlFinal(String nombre, String marca, String modelo, double precio, int stock) {

        Nodo nuevo = new Nodo(nombre, marca, modelo, precio, stock);

        if (inicio == null) {
            inicio = fin = nuevo;
            actual = nuevo;
        } else {
            fin.siguiente = nuevo;
            nuevo.anterior = fin;
            fin = nuevo;
        }
        return null;
    }


    private void actualizarTabla() {
        modelo.setRowCount(0);

        Nodo actual = inicio;
        while (actual != null) {
            modelo.addRow(new Object[]{
                actual.marca,
                actual.nombre,
                actual.modelo,
                actual.precio,
                actual.stock});

            actual = actual.siguiente;
        }
    }

