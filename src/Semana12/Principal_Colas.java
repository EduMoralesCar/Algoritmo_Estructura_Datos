    // Completo
    private void jBtnConstruirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConstruirActionPerformed

        tamaño = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingresar el tamaño de la COLA:"));
        C = new Cola(tamaño);
        jLblTamaño.setText("Tamaño: " + tamaño);
        Mostrar();
    }//GEN-LAST:event_jBtnConstruirActionPerformed

    // Completo
    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        if (C.estaLlenaCola()) {
            JOptionPane.showMessageDialog(null, "La cola está llena");
        } else {
            try {
                int valor = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el valor a agregar:"));
                C.agregar(valor);
                Mostrar();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor ingrese un numero valido");
            }
        }
    }//GEN-LAST:event_jBtnAgregarActionPerformed

    // Completo
    private void jBtnPOS_CimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPOS_CimaActionPerformed
        if (C.estaVaciaCola()) {
            JOptionPane.showMessageDialog(null, "La cola está vacía");
        } else {
            JOptionPane.showMessageDialog(null, "Posicion del elemento en la Cima: " + C.inicio);
        }
    }//GEN-LAST:event_jBtnPOS_CimaActionPerformed

    // Completo
    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
        if (C.estaVaciaCola()) {
            JOptionPane.showMessageDialog(null, "La Cola esta vacia, nose puede eliminar");
        } else {
            int elementoEliminado = C.eliminar();
            JOptionPane.showMessageDialog(null, "Elemento eliminado: " + elementoEliminado);
            Mostrar();
        }
    }//GEN-LAST:event_jBtnEliminarActionPerformed

    // Completo
    private void jLblVaciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLblVaciaMouseClicked
        if (C.estaVaciaCola() == true) {
            JOptionPane.showMessageDialog(null, "La Cola esta vacia");
        } else {
            JOptionPane.showMessageDialog(null, "La Cola no esta vacia. Contiene " + (C.EnCola()) + " elementos");
        }
    }//GEN-LAST:event_jLblVaciaMouseClicked

    // Completo
    private void jBtnVaciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnVaciarActionPerformed
        C.VaciarCola();
        Mostrar();
    }//GEN-LAST:event_jBtnVaciarActionPerformed

