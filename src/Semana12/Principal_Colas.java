    void Mostrar() {
        //Limpiando el TextArea
        jTxtA_PILA.setText(" ");
        //Cargar el TextArea
        for (int i = 0; i < C.VectorCola.length; i++) {
            if (i > C.fin) {
                jTxtA_PILA.append("0 ");
            } else {
                jTxtA_PILA.append(C.VectorCola[i] + " ");
            }
        }
    }

