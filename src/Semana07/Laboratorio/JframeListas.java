    private void ButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConsultarActionPerformed
        String cod = jTextField1.getText();
        if (cod.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un código por favor");
            return;
        }

        pFound = Buscar(inicio, cod);
        if (pFound != null) {
            jTextField2.setText(pFound.nombre);
            jTextField3.setText(pFound.sueldo);
            Habilitar();
        } else {
            JOptionPane.showMessageDialog(this, "El código: " + cod + ", no está en la Lista");
        }
    }//GEN-LAST:event_ButtonConsultarActionPerformed

