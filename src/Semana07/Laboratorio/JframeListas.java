    private void ButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActualizarActionPerformed
        if (pFound != null) {
            pFound.codigo = jTextField1.getText();
            pFound.nombre = jTextField2.getText();
            pFound.sueldo = jTextField3.getText();
            LimpiarEntradas();
            Deshabilitar();
            VerDatos();
        }
    }//GEN-LAST:event_ButtonActualizarActionPerformed

    private void ButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGuardarActionPerformed
        String cod = jTextField1.getText();
        String nom = jTextField2.getText().toUpperCase();
        String suel = jTextField3.getText();

        if (cod.isEmpty() || nom.isEmpty() || suel.isEmpty()) {
            //JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }

        inicio = InsertarAlInicio(inicio, cod, nom, suel);
        LimpiarEntradas();
        VerDatos();
    }//GEN-LAST:event_ButtonGuardarActionPerformed

    private void ButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarActionPerformed
        if (pFound != null) {
            Eliminar(pFound);
            LimpiarEntradas();
            VerDatos();
            Deshabilitar();
            if (inicio == null) {
                JOptionPane.showMessageDialog(this, "La lista está vacía");
            }
        }
    }//GEN-LAST:event_ButtonEliminarActionPerformed

