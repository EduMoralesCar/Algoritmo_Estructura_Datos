    private Nodo Buscar(Nodo inicio, String cod) {
        Nodo actual = inicio;
        while (actual != null && !cod.equalsIgnoreCase(actual.codigo)) {
            actual = actual.siguiente;
        }
        return actual;
    }

    private Nodo InsertarAlInicio(Nodo inicio, String cod, String nom, String suel) {
        Nodo nuevo = new Nodo(cod, nom, suel);
        nuevo.siguiente = inicio;
        inicio = nuevo;
        numero++;
        return inicio;
    }

    private void Eliminar(Nodo actual) {
        if (actual == null) {
            return;
        }

        if (actual == inicio) {
            inicio = inicio.siguiente;
        } else {
            Nodo anterior = inicio;
            while (anterior != null && anterior.siguiente != actual) {
                anterior = anterior.siguiente;
            }
            if (anterior != null) {
                anterior.siguiente = actual.siguiente;
            }
        }
        numero--;
    }

