    private void VerDatos() {
        jTextArea2.setText("NÂ°\tCODIGO\tNOMBRES Y APELLIDOS\tSUELDOS\n\n");
        Nodo actual = inicio;
        int cont = 1;
        while (actual != null) {
            jTextArea2.append(cont + "\t" + actual.codigo + "\t"
                    + actual.nombre + "\t" + actual.sueldo + "\n");
            actual = actual.siguiente;
            cont++;
        }
    }

    private Nodo InsertarAlFinal(Nodo inicio, String cod, String nom, String suel) {
        Nodo nuevo = new Nodo(cod, nom, suel);
        if (inicio == null) {
            inicio = nuevo;
        } else {
            Nodo actual = inicio;
            while (actual.siguiente != null) {
                actual = actual.siguiente;
            }
            actual.siguiente = nuevo;
        }
        numero++;
        return inicio;
    }

