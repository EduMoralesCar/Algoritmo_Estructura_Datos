    private void jbtnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnInsertarActionPerformed
        try {
            nombre = jtxtNombre.getText().trim(); // trim() elimina espacios al inicio y final
            nivel = Integer.parseInt(jtxtNivel.getText().trim());
            poderAtaque = Double.parseDouble(jtxtPoder.getText().trim());

            if (i < P.length) {
                P[i] = new Pokemon(nombre, nivel, poderAtaque);
                i++;
                // Limpiar campos después de insertar (con strings vacíos, no con espacios)
                jtxtNombre.setText("");
                jtxtNivel.setText("");
                jtxtPoder.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Arreglo LLENO");
            }
            Mostrar_en_TABLA();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese valores numéricos válidos para Nivel y Poder", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtnInsertarActionPerformed

