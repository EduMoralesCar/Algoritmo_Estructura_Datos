    private void jbtnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMostrarActionPerformed
        jtxtArea.setText("NOMBRE\tNIVEL\tPODER DE ATAQUE"); // Esto limpia y agrega el encabezado
        for (int j = 0; j < i; j++) {
            jtxtArea.append("\n" + P[j].getNombre() + "\t" + P[j].getNivel() + "\t" + P[j].getPoderAtaque());
        }

    }//GEN-LAST:event_jbtnMostrarActionPerformed

