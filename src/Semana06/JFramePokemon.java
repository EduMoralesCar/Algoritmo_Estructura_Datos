    void Mostrar_en_TABLA() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"Nombre", "Nivel", "Poder Ataque"});

        for (int j = 0; j < i; j++) {
            modelo.addRow(new Object[]{P[j].getNombre(), P[j].getNivel(), P[j].getPoderAtaque()});
        }

        jTable1.setModel(modelo); // Fuera del bucle for

    }

